            <li>
                Vervolgens had hij een computer gekocht en kwam hij erachter dat je leuke dingen kon doen op
                het web. Het enige wat hij nodig had, was een computer en een editor die gratis is.
            </li>
            <li>
                In zijn tijd bestond Flexbox nog niet. Toen het uitkwam, ging hij ermee spelen om te kijken
                wat de mogelijkheden waren, waardoor hij een leuk en chaotisch design had kunnen maken.
            </li>
            <li>
                Hij zegt ook dat het handig is om onzinnige dingen te maken. Door dit te doen kun je coole
                nieuwe dingen maken en je leert er ook van.
            </li>
            <li>
                Hij vond het ook leuk om klokken te maken. Hij deed onderzoek ernaar en er kwamen meer CSS-
                technieken uit, en hij besloot hij verschillende klokken-designs te maken. Hij maakte een
                digitale klok waarbij er mini-klokken omheen waren die draaiden terwijl de tijd van de
                digitale klok veranderde. Ook maakte hij een klok van blokken met verschillende kleuren die
                telden in binair.
            </li>
            <li>
                Hij begon ook aan een project dat heel veel tijd kostte. Dit project besloot hij een klok
                te maken die foto's toont die precies op de aangegeven tijd staan. Hij vond
                toegankelijkheid ook erg belangrijk. Hij vroeg ook aan ons hoeveel minuten er in een klok
                zitten waarbij het antwoord 1440 was. Hij moest hiervoor elke foto een omschrijving meegeven,
                wat super veel werk is. Hij zei ook dat hij in sommige gevallen JavaScript nodig had, waar hij
                zelf niet goed in is, maar hij houdt ervan om uren te nerden op nieuwe technieken, maar CSS is
                waar hij gelukkig van wordt.
            </li>
            <li>
                Hij vertelt ook dat wat toegankelijkheid betreft het belangrijk is dat je goed checkt of
                de website ook werkelijk toegankelijk is en dat niet dingen worden afgevinkt en wordt
                gezegd: "ja, dit is goed genoeg". Er is een site van NPO met allerlei documentaires, waarbij een
                jongen op late leeftijd blind is geworden. Blinde mensen gebruiken screenreaders en hij
                was er niet blij mee toen hij gebruikmaakte van de website. Er was zoveel content op de
                site en alles wordt semantisch opgelezen, zoals "heading 1, heading 2", etc. dat wordt
                voorgelezen door de screenreader en dat is niet fijn voor iemand met een beperking. Hij was
                verbaasd over de reactie en besloot hij de webpagina te redesignen.
            </li>
            <li>
                Hij had nog een site gemaakt voor iemand die blind was, waardoor de gebruiker door de pagina
                heen ging navigeren met de tab. En het duurde zo lang voor hij bij de pagina kwam die hij
                nodig had. Hij moest door veel navigaties heen, waarbij linkjes in zaten die werden
                voorgelezen. En dat kostte voor hem 2 minuten, wat voor gebruikers die geen beperking hebben
                2 seconden kost.
            </li>
            <li>
                Ik vroeg aan hem na de presentatie hoe hij dit had opgelost. Hij deed dit door alle navigatie
                weg te halen en te focussen op de content. Hij vertelde daarbij dat soms de oplossing zo
                simpel is, want veel blinde mensen waar hij mee gewerkt heeft, vragen zich af waarom er
                zoveel navigatielinks zijn die onnodig veel dingen lezen, terwijl zij simpel gebruik willen
                maken van de content die zij nodig hebben.
            </li>
            <li>
                Vasilis vindt dat de zieke dingen die hij heeft gemaakt, wij dat ook kunnen, zolang je het maar
                wilt en bereid bent nieuwe of oude technieken te experimenteren en er "zieke dingen" mee
                maakt.
            </li>
        </ul>
