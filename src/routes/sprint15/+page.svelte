		<h2>Choices and Trade-offs (Kevin Directus)</h2>
		<li>Directus is a backend with customizable functions that can manipulate data.</li>
		<li>
			<strong>User Experience:</strong> Kevin works with a team focused on education to ensure anyone
			can understand it. They also work on community engagement, which is open source on GitHub, making
			Directus user-friendly. Additionally, extensions in the marketplace are created by both Directus
			and the community. These extensions can be downloaded in the marketplace, such as in Visual Studio.
		</li>
		<li>
			Directus servers can be created by users themselves, or users can pay Directus to set one up
			for them.
		</li>
		<li>
			Directus is open source but not entirely. If you build a project with Directus and don’t earn
			much revenue, it’s effectively open source. However, if you make a lot of money, Directus also
			earns a portion of it.
		</li>
		<li>Directus generates revenue through offerings like Directus Cloud, Directus TV, etc.</li>

		<h3>Trade-offs</h3>
		<li>
			<strong>"Fast, Good, or Cheap":</strong> You can only choose two. For example, good and fast might
			be expensive, good and cheap might be slow, and fast and cheap might result in low quality—but
			never all three at the same time. Clients must choose two out of the three options.
		</li>

		<h3>Perfectionism</h3>
		<li>
			<strong>Extension Experts:</strong> They have a team that creates extensions within a set budget.
			These extensions are built based on client needs or to compete with other market players.
		</li>

		<h3>Tech Stack Selection</h3>
		<li>The tech stack can include backend, frontend, and UI libraries.</li>

		<h3>Performance vs Maintenance</h3>
		<li>
			<strong>Performance vs Maintenance:</strong> Achieving more performance often increases maintenance
			costs. Directus prioritizes performance because when large amounts of data are being loaded, it
			must be fast to ensure a good user experience.
		</li>

		<h3>Velocity vs Flexibility</h3>
		<li>
			<strong>Velocity:</strong> Refers to how quickly a team can deliver features, updates, or fixes.
			High velocity often comes from streamlined processes, efficient tools, and minimal bottlenecks.
		</li>
		<li>
			<strong>Flexibility:</strong> Refers to how easily a system or codebase can adapt to changes, such
			as new features, design tweaks, or updates. Flexible systems are modular, scalable, and maintainable.
		</li>

		<h3>Artificial Intelligence</h3>
