    <p>Vervolgens had hij een computer gekocht en kwam hij erachter dat je leuke dingen kon doen op het web. Eenigste wat hij nodig was is een computer en een editor dat gratis is</p>
    <p>In zijn tijd bestond flexbox nog niet. Toen het uit kwam ging hij er mee spelen om te kijken wat de mogelijkheden waren. Waardoor hij een leuke en chaotische design had kunnen maken.</p>
    <p>Hij zegt ook dat het handig is om onzinnige dingen te maken. Door dit te doen kun je coole nieuwe dingen maken en je leert er ook van.</p>
    <p>Hij vondt het ook leuk om klokken te maken. Hij deed onderzoek er naar en er kwamen meer css technieken uit en besloot hij verschillende klokken designs te maken. Hij maakte een digitale klok waarbij er mini klokken om heen waren 
        die draaide terwijl de tijd van de digitale klok veranderde. Ook maakte hij een klok van blokken met verschilende kleuren dat telde telde in binnary.
    </p>
    <p>Hij begon ook aan een project dat het heel veel tijd koste. Dit project besloot hij een klok te maken dat foto's toont dat precies op de aangegeven tijd staat. Hij vondt toegangekelijkheid ook erg belangrijk. 
        Hij vroeg ook aan ons hoeveel minuten er in een klok zitten waarbij het antwoord was 1440. Hij moest hiervoor elke foto een omschrijving meegeven wat super veel werk is. Hij zei ook dat hij in sommige gevalen javascript nodig had
